		<p>A Direcção do Sporting Clube de Braga, sob a presidência de João Gomes Oliveira, reactivou na época desportiva 2001/02 a secção de voleibol, que passou a designar-se Departamento de Voleibol. </p>

		<p>Esta modalidade teve o seu início no ano de 1966, com uma equipa basicamente constituída pelas irmãs Machado (M.ª Alice, M.ª Ondina, M.ª Eugénia, M.ª da Luz e M.ª Augusta), e ainda por outras atletas cujos nomes não são referidos, sob pena de esquecer algum, sendo treinador o Prof. António Basso Marques. </p>
		
		<p>Somente a disputar campeonatos na categoria sénior, a equipa de voleibol sempre dignificou as cores do clube “Arsenal do Minho”, tendo chegado a “rivalizar” com a célebre equipa das “Marias do Benfica” e ainda com o Leixões Sport Clube de Matosinhos. </p>
		
	</div><!-- end .one-third -->

