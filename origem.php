		<h4>O Regresso do Voleibol</h4>

		<p>A Direcção do Sporting Clube de Braga aceitou a proposta efectuada pela Escola EB 2,3 de Lamaçães no sentido de através de uma parceria Escola – Clube, devolver à cidade de Braga o protagonismo que já teve no voleibol e sobretudo contribuir para combater o “deserto” que se verificava ao nível dos desportos colectivos no sector feminino, daquela que por força do progresso é considerada a 3ª cidade do país e um dos concelhos mais jovens da Europa Comunitária. </p>
		
		<p>Na primeira reunião estiverem presentes o Dr. Luís Filipe (director geral do SCB) e o Dr. João Luís Dantas Leite, Dra. Guilhermina Rodrigues e Dr. Daniel Caridade em representação da EB 2,3 de Lamaçães. </p>
		
		<p>A dimensão deste projecto tem tomado proporções nacionais porque é inovador nos seus propósitos, tendo sido já apresentado em várias prelecções nacionais sobre a organização desportiva, pela associação entre escolas básicas e secundárias, no Desporto Escolar, através do Centro de Formação Desportiva de Voleibol da Escola de Lamaçães e, por último, o Sporting Clube de Braga. </p>
